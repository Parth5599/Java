public class Constants {

    public static void main(String[] args) {
        // Constants

        int age = 20;
        age = 21;
        age = 22;
        // age is a not a constant value

        System.out.println(age);

        final float PI = 3.14F;
        // PI = 17; PI is a constant value use "Final" Keyword this value can't change
        System.out.println(PI);
    }
}
