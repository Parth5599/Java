public class Operators {

    public static void main(String[] args) {
        // operators

        int a = 1;
        int b = 2;
        int sum = a + b; // '+,-,/,*' is a Arithmatic opratores
        System.out.println(sum);

        double x = 1;
        double y = 2;
        double modulo = x % y;
        System.out.println(modulo);

        int numb = 1; // '=' is a assignment operators

        System.out.println(numb++); // '++' is Unary Operators
        System.out.println(numb);

    }
}
