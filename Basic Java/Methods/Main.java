package Methods;

public class Main {

    public static void printJava(){ // User Define Function
        System.out.println("Hello Java");
    }
    public static void printName(String name){ // User Define Function
        System.out.println(name);
    }
    public static void printSum(int a, int b){ // User Define Function
        int sum = a + b;
        System.out.println(sum);
    }


    public static void main(String[] args) { // Inbuild Function Or System Function
        //Methods
        printSum(3,6);
    }
}
